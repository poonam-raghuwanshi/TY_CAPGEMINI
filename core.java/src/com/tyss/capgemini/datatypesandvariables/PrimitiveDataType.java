package com.tyss.capgemini.datatypesandvariables;

public class PrimitiveDataType {
	public static void main(String[] args) {
		byte b=12; //declaration & initialization 
		short s=123; //declaration & initialization 
		int i=1234; //declaration & initialization 
		long l=12345123456L; //declaration & initialization 
		float f=12.987F; //declaration & initialization 
		double d=12.987; //declaration & initialization 
		char c='a'; //declaration & initialization 
		boolean isTrue=true; //declaration & initialization 
		System.out.println(b); // utilization
		System.out.println(s); // utilization
		System.out.println(i); // utilization
		System.out.println(l); // utilization
		System.out.println(f); // utilization
		System.out.println(d); // utilization
		System.out.println(c); // utilization
		System.out.println(isTrue); // utilization
		
	}

}
