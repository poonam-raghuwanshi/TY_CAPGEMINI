package com.tyss.capgemini.operators;

public class RelationalOperator {
	public static void main(String[] args) {
		int i = 97;
		int j = 45;

		System.out.println("i less than j: " + (i < j));
		System.out.println("i greater than j: " + (i > j));
		System.out.println("i less than or equal to j: " + (i <= j));
		System.out.println("i greater than or equal to j: " + (i >= j));
		System.out.println("i equal to j: " + (i == j));
		System.out.println("i not equal to j: " + (i != j));

	}
}