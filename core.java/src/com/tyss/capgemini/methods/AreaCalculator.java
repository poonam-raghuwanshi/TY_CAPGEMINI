package com.tyss.capgemini.methods;

public class AreaCalculator {
	
	public int calculateAreaOfSquare(int side) {
		return side * side;
	}
	
	public int calculateAreaOfRactangle(int length, int width) {
		return length * width;
	}
	
	public double  calculateAreaOfCiecle(int radius) {
		return 3.14 * radius * radius;
		
	}
	

}
