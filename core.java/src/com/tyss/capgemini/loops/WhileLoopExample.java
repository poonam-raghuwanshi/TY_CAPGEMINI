package com.tyss.capgemini.loops;

public class WhileLoopExample {
	public static void main(String[] args) {
		int i=1;
		while(i<=10) {
			System.out.println(i*10);
			i++;
		}
		System.out.println("*******INFINITE WHILE LOOP*******	");
		
		// Infinite Loop
		while(true) {
			System.out.println("hello java");
		}
	//	System.out.println();
	}

}
